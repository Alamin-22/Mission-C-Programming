#include <stdio.h>

int main()
{
    int r = 6;

    int height = 10;

    printf("The area of the circle  with radius %d is =>>  ( %f )       \n", r, (3.1416 * r * r));

    printf("The volume of the cylinder  with radius %d and height %d  is  =>>  ( %f )    \n", r, height, (3.1416 * r * r));

    return 0;
}